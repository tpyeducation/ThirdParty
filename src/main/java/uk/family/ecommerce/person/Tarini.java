package uk.family.ecommerce.person;

public class Tarini {

    public int add(int a, int b, int c, int d) {
        int x = a + b + c + d;
        return x;
    }

    public int multiply(int a, int b) {
        int x = a * b;
        return x;
    }

    public int divide(int a, int b) {
        int x = a / b;
        return x;
    }

    public int minus(int a, int b) {
        int x = a - b;
        return x;
    }

    public int reminder (int i, int j) {
      int m = i  % j;
      return m;


    }

}
